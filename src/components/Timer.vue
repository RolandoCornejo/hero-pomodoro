<script>

export default {
  
  data() {
    return {
      pomo: localStorage.getItem("pomodoro"),
      sh: localStorage.getItem("shb"),
      lg: localStorage.getItem("lgb"),
      playing: false,
      pomodoro: true,
      shb: false,
      lgb: false,
      alarm: new Audio(require("@/assets/alarm.wav")),
      oldClock: 0,
      newClock: 0,
      frame1: true,
      frame0: false,
      frame2: false,
      paused: false,
      countdown: null,
      animation: null,
    };
  },
  computed: {
    display() {
      let second = 0;
      if (this.pomodoro) {
        second = this.pomo;
      } else if (this.shb) {
        second = this.sh;
      } else if (this.lgb){
        second = this.lg;
        }else{
          second=0
        }
      //console.log(second)
      let m = Math.floor(second / 60);
      let s = second - m * 60;
      if (m < 10) {
        m = `0${m}`;
      }
      if (s < 10) {
        s = `0${s}`;
      }
      return `${m}:${s}`;
    },
  },
  methods: {
    timer(time) {
      this.playing = true;
      this.paused = false;
      //this.display(time);
      this.animate();
      this.countdown = setInterval(() => {
        if (time > 0) {
          time--;
          //updating the times on-the-fly
          if (this.pomodoro) {
            this.pomo = time;
          } else if (this.shb) {
            this.sh = time;
          } else {
            this.lg = time;
          }
          //console.log(this.pomo)
          //this.display(time);
        } else {
          this.alarm.play();

          //chamging the state of the app (sorta)
          if (this.pomodoro) {
            this.pomo = localStorage.getItem("pomodoro");
            this.pomodoro = false;
          } else if (this.shb) {
            this.sh = localStorage.getItem("shb");
            this.shb = false;
            this.pomodoro = !this.pomodoro;
          } else {
            this.lg = localStorage.getItem("lgb");
            this.lgb = false;
            this.pomodoro = !this.pomodoro;
          }

          this.playing = false;
          clearInterval(this.countdown);
          clearInterval(this.animation);
        }
      }, 1000);
    },
    pause() {
      this.paused = !this.paused;
      if (this.playing) {
        clearInterval(this.countdown);
        clearInterval(this.animation);
        this.frame0 = false;
        this.frame1 = true;
        this.frame2 = false;
        this.playing = false;
      } else {
        this.playing = false;
        if (this.pomodoro) {
          this.timer(this.pomo);
        } else if (this.shb) {
          this.timer(this.sh);
        } else {
          this.timer(this.lg);
        }
      }
      //console.log(this.paused)
    },
    restart() {
      clearInterval(this.animation);
      this.frame0 = false;
      this.frame1 = true;
      this.frame2 = false;
      clearInterval(this.countdown);
      this.playing = false;
      if (this.pomodoro) {
        this.pomo = localStorage.getItem("pomodoro");
        //this.timer(this.pomo);
      } else if (this.shb) {
        this.sh = localStorage.getItem("shb");
        this.timer(this.sh);
      } else {
        this.lg = localStorage.getItem("lgb");
        this.timer(this.lg);
      }
    },
    animate() {
      let counter = 1;
      let end = false;
      this.animation = setInterval(() => {
        if (counter == 0) {
          this.frame0 = true;
          this.frame1 = false;
          this.frame2 = false;
        } else if (counter == 1) {
          this.frame0 = false;
          this.frame1 = true;
          this.frame2 = false;
        } else {
          this.frame0 = false;
          this.frame1 = false;
          this.frame2 = true;
        }
        if (counter == 2) {
          counter = 1;
          end = true;
        } else if (counter == 1) {
          if (end) {
            counter = 0;
          } else {
            counter = 2;
          }
        } else {
          counter = 1;
          end = false;
        }
      }, 175);
    },
  },
  mounted() {
    this.frame1 = true;
    this.frame0 = false;
    this.frame2 = false;
    if (this.pomo == null || this.sh == null || this.lg == null) {
      localStorage.setItem("pomodoro", 1500);
      localStorage.setItem("shb", 300);
      localStorage.setItem("lgb", 600);
      this.pomo = localStorage.getItem("pomodoro");
      this.sh = localStorage.getItem("shb");
      this.lg = localStorage.getItem("lgb");
    }
    //this.display(this.pomo);
  },
};
